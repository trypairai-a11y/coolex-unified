const TONS_TO_KW = 3.51685;
const BTUH_PER_TON = 12000;
const FTWG_TO_KPA = 2.98898;

function tupleToEnglish([capacityTons, compressorKW, waterFlowGPM, waterPressureDropFtWg]: [number, number, number, number]): DHACPerformancePointEnglish {
  return { capacityTons, compressorKW, waterFlowGPM, waterPressureDropFtWg };
}

function expandPerformance(raw: Record<string, Record<string, [number, number, number, number]>>): DHACPerformanceMatrixEnglish {
  const out: DHACPerformanceMatrixEnglish = {};
  for (const [lcwt, byAmb] of Object.entries(raw)) {
    out[lcwt] = {};
    for (const [amb, tuple] of Object.entries(byAmb)) {
      out[lcwt][amb] = tupleToEnglish(tuple);
    }
  }
  return out;
}

function buildModel(modelNumber: string, perfRaw: Record<string, Record<string, [number, number, number, number]>>): DHACModelSpec {
  const performance = expandPerformance(perfRaw);
  // AHRI standard rating: 95°F ambient, 44°F LCWT
  const rated = performance['44']['95'];
  const nominalTons = Math.round(rated.capacityTons * 10) / 10;
  const totalCapacityBtuh = Math.round(rated.capacityTons * BTUH_PER_TON);
  const eer = Math.round((totalCapacityBtuh / (rated.compressorKW * 1000)) * 100) / 100;

  // Structural / non-tabulated approximations scaled by nominal tons
  const lengthIn = Math.round(nominalTons * 7 + 60);
  const widthIn = Math.round(nominalTons * 2.0 + 40);
  const heightIn = Math.round(nominalTons * 1.4 + 56);
  const weightLbs = Math.round(nominalTons * 110 + 350);

  return {
    id: `dhac-${modelNumber}`,
    seriesId: 'dhac',
    modelNumber,
    nominalTons,
    totalCapacityBtuh,
    sensibleCapacityBtuh: totalCapacityBtuh,
    airflowCFM: Math.round(nominalTons * 380),
    powerKW: rated.compressorKW,
    eer,
    leavingDBF: 44,
    leavingWBF: 44,
    compressorCount: nominalTons <= 30 ? 2 : nominalTons <= 60 ? 3 : nominalTons <= 80 ? 4 : 6,
    matchPercent: 100,
    weightLbs,
    lengthIn,
    widthIn,
    heightIn,
    refrigerant: 'R-410A',
    compressorType: 'Scroll',
    performance,
  };
}

export const DHAC_MODELS: DHACModelSpec[] = Object.entries(RAW).map(([modelNumber, perf]) =>
  buildModel(modelNumber, perf),
);

/**
 * Find the two tabulated grid values that bracket a target. Clamps to the
 * range edges when the target is outside the table, so extrapolation reduces
 * to using the boundary value.
 */
function bracket(target: number, options: readonly number[]): { lo: number; hi: number; t: number } {
  const sorted = [...options].sort((a, b) => a - b);
  if (target <= sorted[0]) return { lo: sorted[0], hi: sorted[0], t: 0 };
  if (target >= sorted[sorted.length - 1]) {
    const last = sorted[sorted.length - 1];
    return { lo: last, hi: last, t: 0 };
  }
  for (let i = 0; i < sorted.length - 1; i++) {
    if (target >= sorted[i] && target <= sorted[i + 1]) {
      const lo = sorted[i];
      const hi = sorted[i + 1];
      const t = hi === lo ? 0 : (target - lo) / (hi - lo);
      return { lo, hi, t };
    }
  }
  return { lo: sorted[0], hi: sorted[0], t: 0 };
}

const lerp = (a: number, b: number, t: number) => a + (b - a) * t;

/**
 * Look up a DHAC performance point by leaving chilled water temp and ambient temp (°F).
 * Performs bilinear interpolation across the LCWT × ambient grid; values outside the
 * tabulated range are clamped to the nearest edge. Returns the raw English-unit point.
 */
export function getDHACPerformanceEnglish(
  modelNumber: string,
  lcwtF: number,
  ambientF: number,
): DHACPerformancePointEnglish | undefined {
  const model = DHAC_MODELS.find(m => m.modelNumber === modelNumber);
  if (!model) return undefined;

  const { lo: lcwtLo, hi: lcwtHi, t: tL } = bracket(lcwtF, DHAC_LCWT_F);
  const { lo: ambLo, hi: ambHi, t: tA } = bracket(ambientF, DHAC_AMBIENT_TEMPS_F);

  const p00 = model.performance[String(lcwtLo)]?.[String(ambLo)];
  const p01 = model.performance[String(lcwtLo)]?.[String(ambHi)];
  const p10 = model.performance[String(lcwtHi)]?.[String(ambLo)];
  const p11 = model.performance[String(lcwtHi)]?.[String(ambHi)];
  if (!p00 || !p01 || !p10 || !p11) return undefined;

  const blend = (k: keyof DHACPerformancePointEnglish) => {
    const lo = lerp(p00[k], p01[k], tA);
    const hi = lerp(p10[k], p11[k], tA);
    return lerp(lo, hi, tL);
  };

  return {
    capacityTons: blend('capacityTons'),
    compressorKW: blend('compressorKW'),
    waterFlowGPM: blend('waterFlowGPM'),
    waterPressureDropFtWg: blend('waterPressureDropFtWg'),
  };
}

/**
 * Look up a DHAC performance point and return it in metric units (kW, L/s, kPa)
 * for compatibility with the rest of the chiller pipeline.
 */
export function getDHACPerformance(
  modelNumber: string,
  lcwtF: number,
  ambientF: number,
): DHACPerformancePointMetric | undefined {
  const eng = getDHACPerformanceEnglish(modelNumber, lcwtF, ambientF);
  if (!eng) return undefined;
  return {
    capacityKW: eng.capacityTons * TONS_TO_KW,
    compressorKW: eng.compressorKW,
    waterFlowLPS: gpmToLps(eng.waterFlowGPM),
    waterPressureDropKPa: eng.waterPressureDropFtWg * FTWG_TO_KPA,
  };
}
