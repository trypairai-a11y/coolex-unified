function buildMatrixFor(model: string): ACSC60HzPerformanceMatrix | null {
  const out: ACSC60HzPerformanceMatrix = {};
  for (const lcwt of ACSC_60HZ_LCWT_F) {
    const row = LCWT_TO_TABLE[lcwt][model];
    if (!row) return null;
    out[String(lcwt)] = {};
    ACSC_60HZ_AMBIENT_F.forEach((amb, i) => {
      const cells = row[i];
      out[String(lcwt)][String(amb)] = {
        capacityTons: cells[0],
        compressorKW: cells[1],
        eer: cells[2],
        waterFlowGPM: cells[3],
        waterPressureDropFtWg: cells[4],
      };
    });
  }
  return out;
}

export const ACSC_60HZ_MODELS: readonly string[] = Object.keys(T40);

export const ACSC_60HZ_PERFORMANCE: Record<string, ACSC60HzPerformanceMatrix> =
  Object.fromEntries(
    ACSC_60HZ_MODELS
      .map(m => [m, buildMatrixFor(m)] as const)
      .filter((entry): entry is [string, ACSC60HzPerformanceMatrix] => entry[1] !== null),
  );

function bracket(target: number, options: readonly number[]): { lo: number; hi: number; t: number } {
  const sorted = [...options].sort((a, b) => a - b);
  if (target <= sorted[0]) return { lo: sorted[0], hi: sorted[0], t: 0 };
  if (target >= sorted[sorted.length - 1]) {
    const last = sorted[sorted.length - 1];
    return { lo: last, hi: last, t: 0 };
  }
  for (let i = 0; i < sorted.length - 1; i++) {
    if (target >= sorted[i] && target <= sorted[i + 1]) {
      const lo = sorted[i];
      const hi = sorted[i + 1];
      const t = hi === lo ? 0 : (target - lo) / (hi - lo);
      return { lo, hi, t };
    }
  }
  return { lo: sorted[0], hi: sorted[0], t: 0 };
}

const lerp = (a: number, b: number, t: number) => a + (b - a) * t;

/**
 * Bilinearly interpolate a 60Hz performance point for an ACSC model at the
 * given leaving chilled water temp and ambient (°F). Returns undefined if the
 * model has no 60Hz data.
 */
export function getACSC60HzPerformance(
  modelNumber: string,
  lcwtF: number,
  ambientF: number,
): ACSC60HzPerformancePoint | undefined {
  const matrix = ACSC_60HZ_PERFORMANCE[modelNumber];
  if (!matrix) return undefined;

  const { lo: lcwtLo, hi: lcwtHi, t: tL } = bracket(lcwtF, ACSC_60HZ_LCWT_F);
  const { lo: ambLo, hi: ambHi, t: tA } = bracket(ambientF, ACSC_60HZ_AMBIENT_F);

  const p00 = matrix[String(lcwtLo)]?.[String(ambLo)];
  const p01 = matrix[String(lcwtLo)]?.[String(ambHi)];
  const p10 = matrix[String(lcwtHi)]?.[String(ambLo)];
  const p11 = matrix[String(lcwtHi)]?.[String(ambHi)];
  if (!p00 || !p01 || !p10 || !p11) return undefined;

  const blend = (k: keyof ACSC60HzPerformancePoint) => {
    const lo = lerp(p00[k], p01[k], tA);
    const hi = lerp(p10[k], p11[k], tA);
    return lerp(lo, hi, tL);
  };

  return {
    capacityTons: blend('capacityTons'),
    compressorKW: blend('compressorKW'),
    eer: blend('eer'),
    waterFlowGPM: blend('waterFlowGPM'),
    waterPressureDropFtWg: blend('waterPressureDropFtWg'),
  };
}
