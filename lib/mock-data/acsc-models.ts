export function getACSCModelsMatchingCapacity(requiredTons: number, tolerancePct = 0.2): ACSCModelSpec[] {
  const lower = requiredTons * (1 - tolerancePct);
  const upper = requiredTons * (1 + tolerancePct);
  return ACSC_MODELS
    .filter(m => m.nominalTons >= lower && m.nominalTons <= upper)
    .map(m => ({
      ...m,
      matchPercent: Math.round(100 - Math.abs((m.nominalTons - requiredTons) / requiredTons) * 100),
    }))
    .sort((a, b) => b.matchPercent - a.matchPercent);
}