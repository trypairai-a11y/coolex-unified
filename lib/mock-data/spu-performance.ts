// ── Lookup / interpolation ──────────────────────────────────────────────────

function cellToPoint(c: Cell): SPUPerfPoint {
  return { totalCapacityBtuh: c[0], sensibleCapacityBtuh: c[1], kwInput: c[2] };
}

/** Parse "SPU-420-6R" → { base: "SPU-420", rows: 6 }. */
export function parseSPUModel(modelNumber: string): { base: string; rows: 4 | 6 } | null {
  const m = /^(SPU-\d{3,4})-([46])R$/.exec(modelNumber);
  if (!m) return null;
  return { base: m[1], rows: Number(m[2]) as 4 | 6 };
}

function bracket(target: number, options: readonly number[]): { lo: number; hi: number; t: number } {
  const sorted = [...options].sort((a, b) => a - b);
  if (target <= sorted[0]) return { lo: sorted[0], hi: sorted[0], t: 0 };
  const last = sorted[sorted.length - 1];
  if (target >= last) return { lo: last, hi: last, t: 0 };
  for (let i = 0; i < sorted.length - 1; i++) {
    if (target >= sorted[i] && target <= sorted[i + 1]) {
      const lo = sorted[i];
      const hi = sorted[i + 1];
      return { lo, hi, t: hi === lo ? 0 : (target - lo) / (hi - lo) };
    }
  }
  return { lo: sorted[0], hi: sorted[0], t: 0 };
}

const lerp = (a: number, b: number, t: number) => a + (b - a) * t;

function tableFor(rows: 4 | 6): RawTable {
  return rows === 6 ? RAW_6ROW : RAW_4ROW;
}

function edbAxisFor(rows: 4 | 6): readonly number[] {
  return rows === 6 ? SPU_EDB_6ROW : SPU_EDB_4ROW;
}

/** Interpolate a single (cfm, edb) cell-array down to one ambient-indexed point set. */
function pointAt(edbRow: EdbRow, edbAxis: readonly number[], edbF: number, ambIdx: number): SPUPerfPoint | undefined {
  const { lo, hi, t } = bracket(edbF, edbAxis);
  const cLo = edbRow[lo]?.[ambIdx];
  const cHi = edbRow[hi]?.[ambIdx];
  if (!cLo || !cHi) return undefined;
  const pLo = cellToPoint(cLo);
  const pHi = cellToPoint(cHi);
  return {
    totalCapacityBtuh: lerp(pLo.totalCapacityBtuh, pHi.totalCapacityBtuh, t),
    sensibleCapacityBtuh: lerp(pLo.sensibleCapacityBtuh, pHi.sensibleCapacityBtuh, t),
    kwInput: lerp(pLo.kwInput, pHi.kwInput, t),
  };
}

/**
 * Trilinear SPU performance lookup on (airflow CFM × entering DB °F × condenser
 * ambient °F) for the model's coil-row variant. Values outside the tabulated
 * ranges clamp to the nearest edge.
 */
export function getSPUPerformance(
  modelNumber: string,
  cfm: number,
  edbF: number,
  ambientF: number,
): SPUPerfPoint | undefined {
  const parsed = parseSPUModel(modelNumber);
  if (!parsed) return undefined;
  const cfmRow = tableFor(parsed.rows)[parsed.base];
  if (!cfmRow) return undefined;
  const edbAxis = edbAxisFor(parsed.rows);

  const cfms = Object.keys(cfmRow).map(Number);
  const { lo: cfmLo, hi: cfmHi, t: tC } = bracket(cfm, cfms);
  const { lo: ambLo, hi: ambHi, t: tA } = bracket(ambientF, SPU_AMBIENT_F);
  const iLo = SPU_AMBIENT_F.indexOf(ambLo as (typeof SPU_AMBIENT_F)[number]);
  const iHi = SPU_AMBIENT_F.indexOf(ambHi as (typeof SPU_AMBIENT_F)[number]);

  const rowLo = cfmRow[cfmLo];
  const rowHi = cfmRow[cfmHi];
  if (!rowLo || !rowHi || iLo < 0 || iHi < 0) return undefined;

  // Resolve the four (cfm × ambient) corners, each already edb-interpolated.
  const p00lo = pointAt(rowLo, edbAxis, edbF, iLo);
  const p00hi = pointAt(rowLo, edbAxis, edbF, iHi);
  const p10lo = pointAt(rowHi, edbAxis, edbF, iLo);
  const p10hi = pointAt(rowHi, edbAxis, edbF, iHi);
  if (!p00lo || !p00hi || !p10lo || !p10hi) return undefined;

  const blend = (k: keyof SPUPerfPoint) => {
    const lo = lerp(p00lo[k], p00hi[k], tA);
    const hi = lerp(p10lo[k], p10hi[k], tA);
    return lerp(lo, hi, tC);
  };

  return {
    totalCapacityBtuh: blend('totalCapacityBtuh'),
    sensibleCapacityBtuh: blend('sensibleCapacityBtuh'),
    kwInput: blend('kwInput'),
  };
}

/**
 * Build the 2D panel matrix (CFM × condenser-ambient) at a fixed entering DB,
 * interpolating the DB axis. Used by the performance panel.
 */
export function getSPUMatrix(
  modelNumber: string,
  edbF: number,
): Record<string, Record<string, SPUPerfPoint>> | null {
  const parsed = parseSPUModel(modelNumber);
  if (!parsed) return null;
  const cfmRow = tableFor(parsed.rows)[parsed.base];
  if (!cfmRow) return null;
  const edbAxis = edbAxisFor(parsed.rows);

  const out: Record<string, Record<string, SPUPerfPoint>> = {};
  for (const cfm of Object.keys(cfmRow).map(Number)) {
    out[cfm] = {};
    SPU_AMBIENT_F.forEach((amb, i) => {
      const p = pointAt(cfmRow[cfm], edbAxis, edbF, i);
      if (p) out[cfm][String(amb)] = p;
    });
  }
  return out;
}

/** CFM rows for a given SPU model number (used as the panel's Y axis). */
export function getSPUCfmRows(modelNumber: string): number[] {
  const parsed = parseSPUModel(modelNumber);
  if (!parsed) return [];
  return SPU_CFM_BY_MODEL[parsed.base] ?? [];
}

/** Entering-DB axis (°F) for the panel, given a model's coil-row variant. */
export function getSPUEdbAxis(modelNumber: string): readonly number[] {
  const parsed = parseSPUModel(modelNumber);
  if (!parsed) return SPU_EDB_4ROW;
  return edbAxisFor(parsed.rows);
}

