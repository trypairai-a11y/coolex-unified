/** Parse "NGW-050D4" → { base: "NGW-050", rows: 4 }. */
export function parseNGWModel(modelNumber: string): { base: string; rows: 3 | 4 } | null {
  const m = /^(NGW-\d{3})D([34])$/.exec(modelNumber);
  if (!m) return null;
  return { base: m[1], rows: Number(m[2]) as 3 | 4 };
}

function bracket(target: number, options: readonly number[]): { lo: number; hi: number; t: number } {
  const sorted = [...options].sort((a, b) => a - b);
  if (target <= sorted[0]) return { lo: sorted[0], hi: sorted[0], t: 0 };
  const last = sorted[sorted.length - 1];
  if (target >= last) return { lo: last, hi: last, t: 0 };
  for (let i = 0; i < sorted.length - 1; i++) {
    if (target >= sorted[i] && target <= sorted[i + 1]) {
      const lo = sorted[i];
      const hi = sorted[i + 1];
      return { lo, hi, t: hi === lo ? 0 : (target - lo) / (hi - lo) };
    }
  }
  return { lo: sorted[0], hi: sorted[0], t: 0 };
}

const lerp = (a: number, b: number, t: number) => a + (b - a) * t;

/**
 * Bilinear NGW performance lookup on (airflow CFM × entering-water-temp °F).
 * Values outside the tabulated ranges clamp to the nearest edge.
 */
export function getNGWPerformance(
  modelNumber: string,
  cfm: number,
  ewtF: number,
): NGWPerfPoint | undefined {
  const parsed = parseNGWModel(modelNumber);
  if (!parsed) return undefined;
  const table = parsed.rows === 4 ? RAW_4ROW : RAW_3ROW;
  const cfmRow = table[parsed.base];
  if (!cfmRow) return undefined;

  const cfms = Object.keys(cfmRow).map(Number);
  const { lo: cfmLo, hi: cfmHi, t: tC } = bracket(cfm, cfms);
  const { lo: ewtLo, hi: ewtHi, t: tE } = bracket(ewtF, NGW_EWT_F);

  const iLo = NGW_EWT_F.indexOf(ewtLo as (typeof NGW_EWT_F)[number]);
  const iHi = NGW_EWT_F.indexOf(ewtHi as (typeof NGW_EWT_F)[number]);
  const rowLo = cfmRow[cfmLo];
  const rowHi = cfmRow[cfmHi];
  if (!rowLo || !rowHi || iLo < 0 || iHi < 0) return undefined;

  const p00 = cellToPoint(rowLo[iLo]);
  const p01 = cellToPoint(rowLo[iHi]);
  const p10 = cellToPoint(rowHi[iLo]);
  const p11 = cellToPoint(rowHi[iHi]);

  const blend = (k: keyof NGWPerfPoint) => {
    const lo = lerp(p00[k], p01[k], tE);
    const hi = lerp(p10[k], p11[k], tE);
    return lerp(lo, hi, tC);
  };

  return {
    totalCapacityBtuh: blend('totalCapacityBtuh'),
    sensibleCapacityBtuh: blend('sensibleCapacityBtuh'),
    waterFlowGPM: blend('waterFlowGPM'),
    waterPressureDropFtH2O: blend('waterPressureDropFtH2O'),
  };
}

/** Expose the raw per-model matrix for the performance panel (PerfPoint map). */
export function getNGWMatrix(modelNumber: string): Record<string, Record<string, NGWPerfPoint>> | null {
  const parsed = parseNGWModel(modelNumber);
  if (!parsed) return null;
  const table = parsed.rows === 4 ? RAW_4ROW : RAW_3ROW;
  const cfmRow = table[parsed.base];
  if (!cfmRow) return null;
  const out: Record<string, Record<string, NGWPerfPoint>> = {};
  for (const [cfm, cells] of Object.entries(cfmRow)) {
    out[cfm] = {};
    NGW_EWT_F.forEach((ewt, i) => {
      out[cfm][String(ewt)] = cellToPoint(cells[i]);
    });
  }
  return out;
}

/** CFM rows for a given NGW model number (used as the panel's Y axis). */
export function getNGWCfmRows(modelNumber: string): number[] {
  const parsed = parseNGWModel(modelNumber);
  if (!parsed) return [];
  return NGW_CFM_BY_BASE[parsed.base] ?? [];
}

