const KW_PER_TON = 3.51685;
const BTUH_PER_KW = 3412.142;

function toMatrix(raw: Record<string, Record<string, [number, number, number, number]>>): ACCSTPerformanceMatrix {
  const out: ACCSTPerformanceMatrix = {};
  for (const [lcwt, byAmbient] of Object.entries(raw)) {
    out[lcwt] = {};
    for (const [amb, [cap, comp, wfr, wpd]] of Object.entries(byAmbient)) {
      out[lcwt][amb] = { capacityKW: cap, compressorKW: comp, waterFlowLPS: wfr, waterPressureDropKPa: wpd };
    }
  }
  return out;
}

function buildModel(modelNumber: string, perf: ACCSTPerformanceMatrix): ACCSTModelSpec {
  // Nominal rating point: 35°C ambient, 6.7°C LCWT (matches catalogue's highlighted block)
  const rated = perf['6.7']['35'];
  const nominalTons = Math.round((rated.capacityKW / KW_PER_TON) * 100) / 100;
  const totalCapacityBtuh = Math.round(rated.capacityKW * BTUH_PER_KW);
  const eer = Math.round((totalCapacityBtuh / (rated.compressorKW * 1000)) * 100) / 100;

  // Structural / non-tabulated approximations scaled by nominal tons
  const lengthIn = Math.round(nominalTons * 4 + 90);
  const widthIn = Math.round(nominalTons * 1.2 + 55);
  const heightIn = Math.round(nominalTons * 0.9 + 70);
  const weightLbs = Math.round(nominalTons * 110 + 2200);

  return {
    id: `acc-st-${modelNumber}`,
    seriesId: 'acc-st',
    modelNumber,
    nominalTons,
    totalCapacityBtuh,
    sensibleCapacityBtuh: totalCapacityBtuh,
    airflowCFM: Math.round(nominalTons * 380),
    powerKW: rated.compressorKW,
    eer,
    leavingDBF: 44,
    leavingWBF: 44,
    compressorCount: nominalTons <= 30 ? 2 : nominalTons <= 60 ? 3 : 4,
    matchPercent: 100,
    weightLbs,
    lengthIn,
    widthIn,
    heightIn,
    refrigerant: 'R-407C',
    compressorType: 'Scroll',
    performance: perf,
  };
}

export const ACC_ST_MODELS: ACCSTModelSpec[] = Object.entries(RAW).map(([modelNumber, raw]) =>
  buildModel(modelNumber, toMatrix(raw)),
);

/**
 * Look up an ACC-ST performance point by leaving chilled water temp and ambient temp (°C).
 * Snaps to the closest tabulated condition in each axis.
 */
export function getACCSTPerformance(
  modelNumber: string,
  lcwtC: number,
  ambientC: number,
): ACCSTPerformancePoint | undefined {
  const model = ACC_ST_MODELS.find(m => m.modelNumber === modelNumber);
  if (!model) return undefined;

  const closest = (target: number, options: readonly number[]) =>
    options.reduce((best, v) => Math.abs(v - target) < Math.abs(best - target) ? v : best, options[0]);

  const lcwt = closest(lcwtC, ACC_ST_LCWT_C);
  const ambient = closest(ambientC, ACC_ST_AMBIENT_TEMPS_C);
  return model.performance[String(lcwt)]?.[String(ambient)];
}
