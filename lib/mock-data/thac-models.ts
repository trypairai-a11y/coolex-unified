function tupleToEnglish([capacityTons, compressorKW, waterFlowGPM, waterPressureDropFtWg]: [number, number, number, number]): THACPerformancePointEnglish {
  return { capacityTons, compressorKW, waterFlowGPM, waterPressureDropFtWg };
}

function expandMatrix(raw: Record<string, Record<string, [number, number, number, number]>>): THACPerformanceMatrixEnglish {
  const out: THACPerformanceMatrixEnglish = {};
  for (const lcwt in raw) {
    out[lcwt] = {};
    for (const amb in raw[lcwt]) {
      out[lcwt][amb] = tupleToEnglish(raw[lcwt][amb]);
    }
  }
  return out;
}

function buildModel(modelNumber: string, raw: Record<string, Record<string, [number, number, number, number]>>): THACModelSpec {
  const performance = expandMatrix(raw);

  // Standard rating point: 44°F LCWT, 95°F ambient (T1) — model number = nominal tons.
  const rated = performance['44']['95'];
  const nominalTons = Math.round(rated.capacityTons * 10) / 10;
  const totalCapacityBtuh = Math.round(rated.capacityTons * BTUH_PER_TON);
  const eer = Math.round((totalCapacityBtuh / (rated.compressorKW * 1000)) * 100) / 100;

  // Structural / non-tabulated approximations scaled by nominal tons
  const lengthIn = Math.round(nominalTons * 6 + 50);
  const widthIn = Math.round(nominalTons * 1.8 + 32);
  const heightIn = Math.round(nominalTons * 1.2 + 48);
  const weightLbs = Math.round(nominalTons * 95 + 220);

  return {
    id: `thac-${modelNumber}`,
    seriesId: 'thac',
    modelNumber,
    nominalTons,
    totalCapacityBtuh,
    sensibleCapacityBtuh: totalCapacityBtuh,
    airflowCFM: Math.round(nominalTons * 350),
    powerKW: rated.compressorKW,
    eer,
    leavingDBF: 44,
    leavingWBF: 44,
    compressorCount: nominalTons <= 10 ? 1 : 2,
    matchPercent: 100,
    weightLbs,
    lengthIn,
    widthIn,
    heightIn,
    refrigerant: 'R-410A',
    compressorType: 'Scroll',
    performance,
  };
}

export const THAC_MODELS: THACModelSpec[] = Object.entries(RAW).map(([modelNumber, raw]) =>
  buildModel(modelNumber, raw),
);

function bracket(target: number, options: readonly number[]): { lo: number; hi: number; t: number } {
  const sorted = [...options].sort((a, b) => a - b);
  if (target <= sorted[0]) return { lo: sorted[0], hi: sorted[0], t: 0 };
  if (target >= sorted[sorted.length - 1]) {
    const last = sorted[sorted.length - 1];
    return { lo: last, hi: last, t: 0 };
  }
  for (let i = 0; i < sorted.length - 1; i++) {
    if (target >= sorted[i] && target <= sorted[i + 1]) {
      const lo = sorted[i];
      const hi = sorted[i + 1];
      const t = hi === lo ? 0 : (target - lo) / (hi - lo);
      return { lo, hi, t };
    }
  }
  return { lo: sorted[0], hi: sorted[0], t: 0 };
}

const lerp = (a: number, b: number, t: number) => a + (b - a) * t;

/**
 * Look up THAC performance in native English units (Tons / kW / GPM / ft.wg)
 * by leaving chilled water temp and ambient temp (°F). Bilinearly interpolated;
 * values outside the grid are clamped to the nearest edge.
 */
export function getTHACPerformanceEnglish(
  modelNumber: string,
  lcwtF: number,
  ambientF: number,
): THACPerformancePointEnglish | undefined {
  const model = THAC_MODELS.find(m => m.modelNumber === modelNumber);
  if (!model) return undefined;

  const { lo: lcwtLo, hi: lcwtHi, t: tL } = bracket(lcwtF, THAC_LCWT_F);
  const { lo: ambLo, hi: ambHi, t: tA } = bracket(ambientF, THAC_AMBIENT_TEMPS_F);

  const p00 = model.performance[String(lcwtLo)]?.[String(ambLo)];
  const p01 = model.performance[String(lcwtLo)]?.[String(ambHi)];
  const p10 = model.performance[String(lcwtHi)]?.[String(ambLo)];
  const p11 = model.performance[String(lcwtHi)]?.[String(ambHi)];
  if (!p00 || !p01 || !p10 || !p11) return undefined;

  const blend = (k: keyof THACPerformancePointEnglish) => {
    const lo = lerp(p00[k], p01[k], tA);
    const hi = lerp(p10[k], p11[k], tA);
    return lerp(lo, hi, tL);
  };

  return {
    capacityTons: blend('capacityTons'),
    compressorKW: blend('compressorKW'),
    waterFlowGPM: blend('waterFlowGPM'),
    waterPressureDropFtWg: blend('waterPressureDropFtWg'),
  };
}

/**
 * Metric-unit wrapper around getTHACPerformanceEnglish so the rest of the
 * chiller pipeline (which expects capacityKW / waterFlowLPS / waterPressureDropKPa)
 * can consume THAC results without caring about its English-native catalogue.
 */
export function getTHACPerformance(
  modelNumber: string,
  lcwtF: number,
  ambientF: number,
): THACPerformancePointMetric | undefined {
  const eng = getTHACPerformanceEnglish(modelNumber, lcwtF, ambientF);
  if (!eng) return undefined;
  return {
    capacityKW: eng.capacityTons * TONS_TO_KW,
    compressorKW: eng.compressorKW,
    waterFlowLPS: gpmToLps(eng.waterFlowGPM),
    waterPressureDropKPa: eng.waterPressureDropFtWg * FTWG_TO_KPA,
  };
}
